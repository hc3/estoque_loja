angular.module('loja',['ngRoute','nya.bootstrap.select'])
  .config(function($routeProvider) {
    $routeProvider
      .when('/produtoCad',{
        templateUrl:"views/produto/cadastro.html",
        controller:"ProdutoController",
        controllerAs:"ProdutoController"
      })
      .when('/produtoList',{
        templateUrl:"views/produto/listagem.html",
        controller:"ProdutoController",
        controllerAs:"ProdutoController"
      })
      .when('/emitenteCad',{
        templateUrl:"views/emitente/cadastro.html",
        controller:"EmitenteController",
        controllerAs:"EmitenteController"
      })
      .when('/emitenteList',{
        templateUrl:"views/emitente/listagem.html",
        controller:"EmitenteController",
        controllerAs:"EmitenteController"
      })
      .when('/movEntrada',{
        templateUrl:"views/movimentacao/entrada.html",
        controller:"MovController",
        controllerAs:"MovController"
      })
      .when('/movSaida',{
        templateUrl:"views/movimentacao/saida.html",
        controller:"MovController",
        controllerAs:"MovController"
      })
      .when('/estoque',{
        templateUrl:"views/estoque/consulta.html",
        controller:"EstoqueController",
        controllerAs:"EstoqueController"
      });
  });
